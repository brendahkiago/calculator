#Calculator program
firstnumber = float(input("Enter first number:"))
secondnumber = float(input("Enter second number:"))
myoperator = str(input("Enter operator(+, -, *, /)"))

if myoperator=="+":
    print(firstnumber+secondnumber)
elif myoperator=="_":
    print(firstnumber-secondnumber)
elif myoperator=="*":
    print(firstnumber-secondnumber)
elif myoperator=="/":
    print(firstnumber/secondnumber)













